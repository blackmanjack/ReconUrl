function encodePunycode(char) {
  try {
    return new URL("http://" + char + ".com").hostname.split(".")[0];
  } catch {
    return null;
  }
}

export default function PunycodeGenerator() {
  const [letter, setLetter] = useState("");
  const [results, setResults] = useState([]);

  const generatePunycodeVariants = () => {
    const lower = letter.toLowerCase();
    const glyphs = homoglyphsMap[lower] || [];
    if (!glyphs.length) {
      setResults([{ glyph: "âŒ", punycode: "No homoglyphs found." }]);
      return;
    }
    const punyVariants = glyphs.map((glyph) => ({
      glyph,
      punycode: encodePunycode(glyph),
    }));
    setResults(punyVariants);
  };

  return (
    <div className="wrapper">
      <div className="container">
        <h1 className="title">Punycode Homoglyph Generator</h1>
        <div className="button-list">
          <input
            type="text"
            value={letter}
            onChange={(e) => setLetter(e.target.value)}
            maxLength={1}
            placeholder="Enter a letter (a-z)"
            className="input"
          />
          <button onClick={generatePunycodeVariants} className="btn">
            Generate Variants
          </button>

          <div className="result">
            {results.map((res, idx) => (
              <div key={idx} className="text-sm">
                {res.glyph} â†’ {res.punycode}
              </div>
            ))}
          </div>
        </div>
      </div>
    </div>
  );
}
